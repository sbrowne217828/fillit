#include "libft.h"

char	*ft_strdup(const char *s)
{
	unsigned int	i;
	size_t			len;
	char			*str;

	len = ft_strlen(s);
	if (!(str = (char*)malloc(sizeof(*str) * len + 1)))
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
